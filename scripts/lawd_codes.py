SIDO_ORDER = ["서울", "경기", "부산", "대구", "인천", "광주", "대전", "울산", "세종"]


def all_lawd_list():
    """Return flat list of all LAWD codes in consistent order."""
    result = []
    for sido in SIDO_ORDER:
        result.extend(sorted(LAWD_CODES[sido].keys()))
    return result


def sido_for_lawd(code):
    """Given a 5-digit LAWD code, return the 시도 name."""
    for sido, districts in LAWD_CODES.items():
        if code in districts:
            return sido
    return None


def lawd_name(code):
    """Given a 5-digit LAWD code, return the 시군구 name."""
    for districts in LAWD_CODES.values():
        if code in districts:
            return districts[code]
    return code
